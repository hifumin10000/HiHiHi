    public void LoadData(){
        try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            int number;
            Vector row,column ;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from Benh");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();//tra ve so cot
            for(int i = 1 ;i <= number; i++){
                column.add(metadata.getColumnName(i));
            }
            T.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<= number; i++){
                    row.addElement(rs.getString(i));
                }
                T.addRow(row);
                Benh.setModel(T);     
            }
                       
        Benh.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent lse) {
                    if(Benh.getSelectedRow() >=0 ){
                      
                        TB.setText(Benh.getValueAt(Benh.getSelectedRow(),1) + "");   
                        TC.setText(Benh.getValueAt(Benh.getSelectedRow(),2) + "");       
                    }
                }
            });
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }
