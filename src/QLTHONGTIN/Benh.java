    private void SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuaActionPerformed
        // TODO add your handling code here:
        try {
            Connect a = new Connect();
            Connection conn = a.getConnection();
            int index=Benh.getSelectedRow();
            String value=Benh.getModel().getValueAt(index,0).toString();
            PreparedStatement pre = conn.prepareStatement("Update Benh set [Tên Bệnh]=?,[Triệu Chứng]=? where [Mã Bệnh]='"+value+"'");
            pre.setString(1, TB.getText());
            pre.setString(2, TC.getText());
           
            
            int chk = pre.executeUpdate();
            if(chk>0){
                JOptionPane.showMessageDialog(this,"Sửa Thành Công");
            }
            T.setRowCount(0);
            LoadData();
            conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Sửa Thất Bại! Xin Hãy Kiểm Tra Lại","Lỗi",1);
            System.out.println(ex.toString());
        }
        TB.setText("");
        TC.setText("");
    }//GEN-LAST:event_SuaActionPerformed

